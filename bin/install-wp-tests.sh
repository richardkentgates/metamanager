#!/usr/bin/env bash
# install-wp-tests.sh
#
# Installs the WordPress PHPUnit test environment.
# Adapted from the official WordPress test setup script.
#
# Usage:
#   bin/install-wp-tests.sh <db-name> <db-user> <db-pass> [db-host] [wp-version] [skip-db-create]
#
# Example:
#   bin/install-wp-tests.sh wordpress_test wordpress '#GCM@MySQL#' localhost latest
#
# Environment variables:
#   WP_TESTS_DIR  — where to install the test library (default: /tmp/wordpress-tests-lib)
#   WP_CORE_DIR   — where to install WP itself        (default: /tmp/wordpress)

set -e

DB_NAME="${1:-wordpress_test}"
DB_USER="${2:-root}"
DB_PASS="${3:-}"
DB_HOST="${4:-localhost}"
WP_VERSION="${5:-latest}"
SKIP_DB_CREATE="${6:-false}"

WP_TESTS_DIR="${WP_TESTS_DIR:-/tmp/wordpress-tests-lib}"
WP_CORE_DIR="${WP_CORE_DIR:-/tmp/wordpress}"

# -----------------------------------------------------------------------
# Helpers
# -----------------------------------------------------------------------

download() {
	if command -v curl >/dev/null; then
		curl --silent --fail "$1" >"$2"
	elif command -v wget >/dev/null; then
		wget --quiet -O "$2" "$1"
	else
		echo "ERROR: neither curl nor wget found" >&2
		exit 1
	fi
}

# -----------------------------------------------------------------------
# Resolve WP version tag
# -----------------------------------------------------------------------

if [[ "$WP_VERSION" == "latest" ]]; then
	LATEST_VERSION_URL="https://api.wordpress.org/core/version-check/1.7/"
	LOCAL_JSON="/tmp/wp-latest.json"
	download "$LATEST_VERSION_URL" "$LOCAL_JSON"
	WP_VERSION=$(grep -o '"version":"[^"]*"' "$LOCAL_JSON" | sed 's/"version":"//;s/"//' | head -1)
	if [[ -z "$WP_VERSION" ]]; then
		echo "ERROR: could not determine latest WP version" >&2
		exit 1
	fi
	echo "[INFO]  Latest WordPress version: $WP_VERSION"
fi

WP_TESTS_TAG="tags/${WP_VERSION}"
WP_MAIN_TAG="tags/${WP_VERSION}"

# -----------------------------------------------------------------------
# Install WP core files (needed by test bootstrap)
# -----------------------------------------------------------------------

if [[ ! -d "$WP_CORE_DIR" ]]; then
	mkdir -p "$WP_CORE_DIR"
	echo "[INFO]  Downloading WordPress $WP_VERSION core files..."
	download "https://wordpress.org/wordpress-${WP_VERSION}.tar.gz" /tmp/wordpress.tar.gz
	tar --strip-components=1 -zxf /tmp/wordpress.tar.gz -C "$WP_CORE_DIR"
fi

# -----------------------------------------------------------------------
# Install WP test library
# -----------------------------------------------------------------------

if [[ ! -d "$WP_TESTS_DIR/includes" ]]; then
	mkdir -p "$WP_TESTS_DIR"
	echo "[INFO]  Downloading WP test library (${WP_TESTS_TAG})..."
	SVN_BASE="https://develop.svn.wordpress.org/${WP_TESTS_TAG}"

	# Try svn first; fall back to a GitHub tarball
	if command -v svn >/dev/null 2>&1; then
		svn export --quiet "${SVN_BASE}/tests/phpunit/includes/"  "${WP_TESTS_DIR}/includes"
		svn export --quiet "${SVN_BASE}/tests/phpunit/data/"      "${WP_TESTS_DIR}/data"
	else
		echo "[INFO]  svn not found — downloading via GitHub tarball..."
		TARBALL_URL="https://github.com/WordPress/wordpress-develop/archive/refs/tags/${WP_VERSION}.tar.gz"
		download "$TARBALL_URL" /tmp/wp-develop.tar.gz
		TAR_DIR=$(tar -tzf /tmp/wp-develop.tar.gz | head -1 | cut -d/ -f1)
		mkdir -p /tmp/wp-develop-src
		tar -zxf /tmp/wp-develop.tar.gz -C /tmp/wp-develop-src
		cp -r "/tmp/wp-develop-src/${TAR_DIR}/tests/phpunit/includes" "${WP_TESTS_DIR}/includes"
		mkdir -p "${WP_TESTS_DIR}/data"
		cp -r "/tmp/wp-develop-src/${TAR_DIR}/tests/phpunit/data/."   "${WP_TESTS_DIR}/data/"
		rm -rf /tmp/wp-develop-src
	fi
fi

# -----------------------------------------------------------------------
# Write wp-tests-config.php
# -----------------------------------------------------------------------

if [[ ! -f "${WP_TESTS_DIR}/wp-tests-config.php" ]]; then
	cat > "${WP_TESTS_DIR}/wp-tests-config.php" <<PHP
<?php
/* WP Test Suite Config — generated by bin/install-wp-tests.sh */
define( 'ABSPATH',        '${WP_CORE_DIR}/' );
define( 'DB_NAME',        '${DB_NAME}' );
define( 'DB_USER',        '${DB_USER}' );
define( 'DB_PASSWORD',    '${DB_PASS}' );
define( 'DB_HOST',        '${DB_HOST}' );
define( 'DB_CHARSET',     'utf8' );
define( 'DB_COLLATE',     '' );
\$table_prefix = 'wptests_';
define( 'WP_TESTS_DOMAIN',     'example.org' );
define( 'WP_TESTS_EMAIL',      'admin@example.org' );
define( 'WP_TESTS_TITLE',      'Test Blog' );
define( 'WP_PHP_BINARY',       'php' );
define( 'WPLANG',              '' );
PHP
	echo "[OK]    wp-tests-config.php written to ${WP_TESTS_DIR}"
fi

# -----------------------------------------------------------------------
# Create the test database
# -----------------------------------------------------------------------

if [[ "$SKIP_DB_CREATE" != "true" ]]; then
	echo "[INFO]  Creating test database '${DB_NAME}'..."
	mysqladmin create "$DB_NAME" --user="$DB_USER" --password="$DB_PASS" --host="$DB_HOST" 2>/dev/null \
		|| echo "[WARN]  Database '${DB_NAME}' may already exist — continuing."
fi

echo ""
echo "============================================================"
echo "  WordPress test environment installed!"
echo "  Tests dir: ${WP_TESTS_DIR}"
echo "  Core dir:  ${WP_CORE_DIR}"
echo "  Database:  ${DB_NAME}"
echo "============================================================"
echo ""
echo "  Run tests with:  vendor/bin/phpunit"
echo ""
