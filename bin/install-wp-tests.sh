#!/usr/bin/env bash
# install-wp-tests.sh
#
# Configures the WordPress PHPUnit test environment for Metamanager.
#
# This script does NOT download the WP test library — that comes via the
# wp-phpunit/wp-phpunit Composer package already in vendor/.
#
# What this script does:
#   1. Creates the test database (unless SKIP_DB_CREATE=true)
#   2. Writes tests/wp-tests-config.php with the supplied DB credentials
#      and the path to the WordPress installation
#
# Usage:
#   bin/install-wp-tests.sh <db-name> <db-user> <db-pass> [db-host] [wp-path] [skip-db-create]
#
# Examples:
#   # Development server (WP already installed):
#   bin/install-wp-tests.sh wordpress_test wordpress 'secret' localhost /srv/www/wordpress
#
#   # CI (WP downloaded to /tmp/wordpress):
#   bin/install-wp-tests.sh wordpress_test root '' 127.0.0.1 /tmp/wordpress

set -e

DB_NAME="${1:-wordpress_test}"
DB_USER="${2:-root}"
DB_PASS="${3:-}"
DB_HOST="${4:-localhost}"
WP_PATH="${5:-/tmp/wordpress}"
SKIP_DB_CREATE="${6:-false}"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")/.." && pwd)"
TESTS_DIR="${SCRIPT_DIR}/tests"

# -----------------------------------------------------------------------
# Create the test database
# -----------------------------------------------------------------------

if [[ "$SKIP_DB_CREATE" != "true" ]]; then
	echo "[INFO]  Creating test database '${DB_NAME}'..."
	mysqladmin create "$DB_NAME" \
		--user="$DB_USER" \
		--password="$DB_PASS" \
		--host="$DB_HOST" 2>/dev/null \
		|| echo "[WARN]  Database '${DB_NAME}' may already exist — continuing."
fi

# -----------------------------------------------------------------------
# Write tests/wp-tests-config.php
# -----------------------------------------------------------------------

cat > "${TESTS_DIR}/wp-tests-config.php" <<PHP
<?php
/* Generated by bin/install-wp-tests.sh — do not edit by hand. */
define( 'ABSPATH',        rtrim( '${WP_PATH}', '/' ) . '/' );
define( 'DB_NAME',        '${DB_NAME}' );
define( 'DB_USER',        '${DB_USER}' );
define( 'DB_PASSWORD',    '${DB_PASS}' );
define( 'DB_HOST',        '${DB_HOST}' );
define( 'DB_CHARSET',     'utf8' );
define( 'DB_COLLATE',     '' );
\$table_prefix             = 'wptests_';
define( 'WP_TESTS_DOMAIN', 'example.org' );
define( 'WP_TESTS_EMAIL',  'admin@example.org' );
define( 'WP_TESTS_TITLE',  'Test Blog' );
define( 'WP_PHP_BINARY',   'php' );
define( 'WPLANG',          '' );
PHP

echo ""
echo "============================================================"
echo "  WordPress test environment configured!"
echo "  Config: ${TESTS_DIR}/wp-tests-config.php"
echo "  WP:     ${WP_PATH}"
echo "  DB:     ${DB_NAME} @ ${DB_HOST}"
echo "============================================================"
echo ""
echo "  Run tests with:  vendor/bin/phpunit"
echo ""
